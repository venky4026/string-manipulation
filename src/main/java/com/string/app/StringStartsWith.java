package com.string.app;

public class StringStartsWith {

	public static void main(String[] args) {

		String str = "Hello World";

		if (str.startsWith("H")) {
			System.out.println("Start with H");
		} else {
			System.out.println("Not Started with H");
		}

		if (str.endsWith("d")) {
			System.out.println("Ends with d");
		} else {
			System.out.println("Ends with d");
		}
	}
}
